    public enum EawVal
    {
        /// <summary>
        /// Ambiguous (ex. 'α')
        /// </summary>
        A,
        /// <summary>
        /// Fullwidth (ex. 'Ａ')
        /// </summary>
        F,
        /// <summary>
        /// Halfwidth (ex. 'ｱ')
        /// </summary>
        H,
        /// <summary>
        /// Neutral (ex. 'À')
        /// </summary>
        N,
        /// <summary>
        /// Narrow (ex. 'A')
        /// </summary>
        Na,
        /// <summary>
        /// Wide (ex. 'ア')
        /// </summary>
        W,
    }

    public static class EastAsianWidth
    {

        /// <summary>
        /// 東アジア圏のレガシーな文字セット(Shift_JISなど)における文字幅
        /// Ambiguousを文字幅2とするカウント法
        /// </summary>
        public static int legacyWidth(string s) => width(s, (EawVal mode) =>
        {
            switch (mode)
            {
                // Fullwidth (ex. 'Ａ')
                case EawVal.F: return 2;
                // Wide (ex. 'ア')
                case EawVal.W: return 2;
                // Halfwidth (ex. 'ｱ')
                case EawVal.H: return 1;
                // Narrow (ex. 'A')
                case EawVal.Na: return 1;
                // Ambiguous (ex. 'α')
                case EawVal.A: return 2;
                // Neutral (ex. 'À')
                case EawVal.N: return 1;
                default: return 0;
            }
        });

        /// <summary>
        /// 東アジア圏以外での文字幅
        /// Ambiguousを文字幅1とするカウント法
        /// </summary>
        public static int modernWidth(string s) => width(s, (EawVal mode) =>
        {
            switch (mode)
            {
                // Fullwidth (ex. 'Ａ')
                case EawVal.F: return 2;
                // Wide (ex. 'ア')
                case EawVal.W: return 2;
                // Halfwidth (ex. 'ｱ')
                case EawVal.H: return 1;
                // Narrow (ex. 'A')
                case EawVal.Na: return 1;
                // Ambiguous (ex. 'α')
                case EawVal.A: return 1;
                // Neutral (ex. 'À')
                case EawVal.N: return 1;
                default: return 0;
            }
        });

        public static int width(string str, Func<EawVal, int> widthDef)
        {
            // Unicode正規形Cによる正規化
            //（例えばMacのファイルシステムではUnicode正規化Dの変種が用いられているので、その影響を緩和）
            string normStr = str.Normalize();
            int len = 0;
            // サロゲートペアについて考慮する。U+20BB7「𠮷」（よし）、U+29E3D「𩸽」（ほっけ）、U+1F914「🤔」(thinking face)など。
            for (var i = 0; i < normStr.Length; i += (char.IsSurrogate(normStr, i) ? 2 : 1))
            {
                int code = char.ConvertToUtf32(normStr, i);
                // 二分探索
                var low = 0;
                var high = eawData.Length;
                while (low < high)
                {
                    var mid = (low + high) >> 1;
                    if (code > eawData[mid].high)
                    {
                        low = mid + 1;
                        continue;
                    }
                    if (code < eawData[mid].low)
                    {
                        high = mid;
                        continue;
                    }
                    len += widthDef(eawData[mid].mode);
                    // Console.Out.WriteLine(String.Format("{0} {1:X4} {2}", char.ConvertFromUtf32(code), code, eawData[mid].mode));
                    goto success;
                }
                // 表外の文字はNeutralとして扱う
                len += widthDef(EawVal.N);
                success:;
            }
            return len;
        }

